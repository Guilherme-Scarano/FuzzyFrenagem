    void fuzzifica(){
        fis.setVariable("Distancia", sensorDistancia);
        fis.setVariable("Velocidade", sensorVelocidade);
        fis.evaluate();
        Variable resp = fis.getVariable("pressao");
        Double saida = resp.getValue();
        jLabelpressao.setText(String.valueOf(saida));
        
        dialog.repaint();
    }

